using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace 코드의_흐름제어_for
{
    class Program
    {
        static void Main(string[] args)
        {
            //for (초기화식; 조건식; 반복식)
            for (int i = 0; i < 5; i++)
            {
                Console.WriteLine("Hello World!");
            }
        }
    }
}
